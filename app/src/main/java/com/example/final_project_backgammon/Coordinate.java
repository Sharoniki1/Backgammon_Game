package com.example.final_project_backgammon;

public class Coordinate {
    public int x;
    public int y;


    public Coordinate(){}


    public int getX() {
        return x;
    }

    public Coordinate setX(int x) {
        this.x = x;
        return this;
    }

    public int getY() {
        return y;
    }

    public Coordinate setY(int y) {
        this.y = y;
        return this;
    }
}
